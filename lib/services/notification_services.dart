    // tz.setLocalLocation(tz.getLocation(timeZone));
    final String indiaTimeZoneName = 'Asia/Kolkata';
    tz.setLocalLocation(tz.getLocation(indiaTimeZoneName));
  }

  Future<String> getTimeZone() async {
    final String localTimeZone = tz.local.name;
    return localTimeZone;
  }

  Future<void> displayNotification(
      {required String title, required String body}) async {
    print("doing test");
    var androidPlatformChannelSpecifics = new AndroidNotificationDetails(
        'your channel id', 'your channel name',
        channelDescription: 'your channel description',
        importance: Importance.max,
        priority: Priority.high);
    var iOSPlatformChannelSpecifics = DarwinNotificationDetails();
    var platformChannelSpecifics = new NotificationDetails(
        android: androidPlatformChannelSpecifics,
        iOS: iOSPlatformChannelSpecifics);
    await flutterLocalNotificationsPlugin.show(
      0,
      title,
      body,
      platformChannelSpecifics,
      payload: title,
    );
  }

  void requestIOSPermissions() {
    flutterLocalNotificationsPlugin
        .resolvePlatformSpecificImplementation<
            IOSFlutterLocalNotificationsPlugin>()
        ?.requestPermissions(
          alert: true,
          badge: true,
          sound: true,
        );
  }

  Future selectNotification(NotificationResponse? response) async {
    final String? payload = response?.payload;
    if (payload != null) {
      print('notification payload: $payload');
    } else {
      print("Notification Done");
    }

    if (payload == "Theme Change") {
      print("Nothing to navigate");
    } else {
      Get.to(() => NotifiedPage(label: payload));
    }
  }

